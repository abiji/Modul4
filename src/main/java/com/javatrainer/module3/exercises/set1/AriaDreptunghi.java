package com.javatrainer.module3.exercises.set1;

public class AriaDreptunghi {

    public static int ariaDreptunghi(final int lungime, final int latime)
    {
        return lungime*latime;
    }

    public static void main(String[] args) {
        System.out.println("Aria dreptunghiului:" + ariaDreptunghi(10,5));
    }
}
